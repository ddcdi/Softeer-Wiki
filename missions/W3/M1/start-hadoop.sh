#!/bin/bash

# ---------------------------------------------------------
# 1. SSH 데몬 시작
#    3~5단계에서 키/설정은 다 준비해뒀고, 여기서 실제로 서비스를 켬
# ---------------------------------------------------------
service ssh start

# ---------------------------------------------------------
# 2. NameNode 포맷 (최초 1회만!)
#    - /root/hdfs/namenode 안에 이미 포맷된 흔적(current 디렉토리)이 있으면
#      다시 포맷하지 않음. 재포맷하면 기존 HDFS 메타데이터가 초기화되어
#      기존에 올려둔 데이터를 잃어버림.
#    - 컨테이너를 처음 띄우는 경우에만 이 if문 안으로 들어가게 됨
# ---------------------------------------------------------
if [ ! -d "/root/hdfs/namenode/current" ]; then
    echo ">>> NameNode가 아직 포맷되지 않음. 최초 포맷을 진행합니다."
    hdfs namenode -format -force
else
    echo ">>> 이미 포맷된 NameNode 발견. 포맷을 건너뜁니다."
fi

# ---------------------------------------------------------
# 3. HDFS 데몬 시작 (NameNode, DataNode, SecondaryNameNode)
# ---------------------------------------------------------
start-dfs.sh

# ---------------------------------------------------------
# 4. YARN 데몬 시작 (ResourceManager, NodeManager)
# ---------------------------------------------------------
start-yarn.sh

# ---------------------------------------------------------
# 5. 상태 확인 (로그로 남겨서 docker logs로 확인 가능하게)
# ---------------------------------------------------------
echo ">>> 실행 중인 Java 프로세스 목록:"
jps

# ---------------------------------------------------------
# 6. 컨테이너를 살려두기
#    위 데몬들은 전부 백그라운드 프로세스로 뜨기 때문에,
#    스크립트가 여기서 그냥 끝나버리면 CMD 프로세스가 종료되고
#    Docker는 컨테이너를 바로 꺼버림.
#    tail -f /dev/null 로 "아무것도 안 하지만 종료되지 않는" 프로세스를
#    포그라운드에 잡아둬서 컨테이너를 계속 살려둠.
# ---------------------------------------------------------
tail -f /dev/null